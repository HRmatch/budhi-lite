function getLang(){
  return sessionStorage.getItem('budhi_lite_lang') || 'en'
}
function setLang(lang){
  sessionStorage.setItem('budhi_lite_lang',
  lang || 'en')
}
function t(key){
  const lang=getLang();
  return (I18N[
    lang
  ]&&I18N[
    lang
  ][
    key
  ])||I18N.en[
    key
  ]||key
}
function ml(obj){
  const lang=getLang();
  if(obj&&typeof obj==='object'&&!Array.isArray(obj)) return obj[
    lang
  ]||obj.en||obj.pt||Object.values(obj)[
    0
  ]||'';
  return obj??''
}
